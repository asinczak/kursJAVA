package pl.com.ttpsc.kursJava.wyklad_4;

public class Kalkulator {

    int suma (double i, double y) {
        int wynikSumy = (int) (i + y);
        return wynikSumy;
    }


    int roznica (int a, int b) {
        int wynikRoznicy = a - b;
        return wynikRoznicy;
    }


    int iloczyn (int x, int z) {
        int wynikIloczynu = x * z;
        return wynikIloczynu;
    }


    int iloraz (int c, int d) {
        int wynikIlorazu = c / d;
        return wynikIlorazu;
    }
}
